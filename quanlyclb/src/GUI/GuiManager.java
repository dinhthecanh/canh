    boolean ktraTrungMaSv(String a)
    {
        boolean ok = true;
        for(sinhVien i:listSV)
        {
            if(i.getMaSV().equalsIgnoreCase(a))
                ok = false;
        }
        return ok;
    }
    sinhVien returnSinhVien(String b)
    {
        sinhVien a = new sinhVien();
        for(sinhVien i :listSV)
        {
            if(i.getMaSV().equals(b))
            {
                a= i;
            }
        }
        return a;
    }
   
